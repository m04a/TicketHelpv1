    {
        $oauthFields = Setting::where('id',1)->first()->toArray();
        $oauthData = Setting::find(1);
        $oauth = $request['oauth'];
        foreach($oauth as  $key => $value) {
            if ($value == 'on') {
                $oauth[$key] = 1;
            }
        }
        $oauthData->fill($oauth);
        $oauthData->save();
